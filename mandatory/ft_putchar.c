#include "ft_printf.h"
ssize_t	ft_putchar(char c, t_cout *s)
{
	if (s -> sign == 1)
		return (-1);
	if (s -> index > INT_MAX)
	{
		s -> sign = 1;
		return (-1);
	}
	s -> index++;
	return (write(1, &c, 1));
}
