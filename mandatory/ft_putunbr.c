#include "ft_printf.h"
ssize_t	ft_putunbr(unsigned int n, t_cout *s)
{
	if (s -> sign == 0)
	{
		if (n > 9)
		{
			ft_putnbr(n / 10, s);
		}
		ft_putchar('0' + n % 10, s);
	}
	else
		return (-1);
	return (1);
}
